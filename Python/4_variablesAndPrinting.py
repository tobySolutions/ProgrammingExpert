# Variables
'''
 Variables in Python:
 
 - Cannot begin with a number
 
 - Cannot contain any special characters other than underscore; which is used to separate words (Snake-case). It's not conventional to use camel-case in Python.
 
- Cannot contain any spaces
'''
number = 5
number_in_words = "ten"

print(number, number_in_words)






